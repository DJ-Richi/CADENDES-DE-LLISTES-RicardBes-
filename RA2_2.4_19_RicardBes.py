# Descripció: Fes una llista de números i multiplica tots els elements per 2.
# Especificacions Programa que fagi una llista de numeros i es multiplica tots els elements per el doble.

# Crear una llista de números
llista = [1, 2, 3, 4, 5]

# Multiplicar cada element per 2
llista_doble = []
for num in llista:
    llista_doble.append(num * 2)

# Mostrar el resultat
print(llista_doble)